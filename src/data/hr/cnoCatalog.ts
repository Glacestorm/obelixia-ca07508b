// Función para buscar CNO por código o descripción
export function searchCNO(query: string, maxResults = 20): CNOCode[] {
  const normalizedQuery = query.toLowerCase().trim();
  
  return CNO_CATALOG
    .filter(cno => 
      cno.code.includes(normalizedQuery) || 
      cno.description.toLowerCase().includes(normalizedQuery)
    )
    .slice(0, maxResults);
}

// Función para obtener CNO por código exacto
export function getCNOByCode(code: string): CNOCode | undefined {
  return CNO_CATALOG.find(cno => cno.code === code);
}

// Función para obtener grupos principales
export function getCNOGroups(): CNOCode[] {
  return CNO_CATALOG.filter(cno => cno.groupLevel === 1);
}

// Función para obtener ocupaciones de un grupo
export function getCNOByGroup(groupCode: string): CNOCode[] {
  return CNO_CATALOG.filter(cno => 
    cno.parentCode === groupCode && cno.groupLevel === 4
  );
}

export default CNO_CATALOG;
